function transitionKey(from: RotationIndex, to: RotationIndex): string {
  return `${from}>${to}`
}

export function getSrsKicks(
  type: TetrominoType,
  from: RotationIndex,
  to: RotationIndex,
): Point[] {
  if (type === 'O') return [{ x: 0, y: 0 }]
  const key = transitionKey(from, to)
  if (type === 'I') {
    return I_KICKS[key] ?? [{ x: 0, y: 0 }]
  }
  return JLSTZ_KICKS[key] ?? [{ x: 0, y: 0 }]
}

export function nextRotationCW(r: RotationIndex): RotationIndex {
  return ((r + 1) % 4) as RotationIndex
}

export function nextRotationCCW(r: RotationIndex): RotationIndex {
  return ((r + 3) % 4) as RotationIndex
}
